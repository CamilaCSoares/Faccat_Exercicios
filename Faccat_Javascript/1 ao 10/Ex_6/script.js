document.write(`<p>6) Escreva um algoritmo para ler as dimensões de um retângulo (base e altura), calcular e escrever a
área do retângulo.</p>`)

var altura = Number(prompt("Digite a altura do triangulo em metros: "))

var base = Number(prompt("Digite a base do triangulo em metros:"))

var area = altura * base / 2

alert(`A área do triângulo em mentros é: ${area}`)